SELECT 
    e.EmployeeID,
    e.FIRSTNAME,
    COALESCE(S.totalSales, 0) AS TotalSales
FROM 
    Employees e
JOIN 
    (SELECT 
         EmployeeID, 
         SUM(SaleSAmount) AS totalSales
     FROM 
         Sales
     GROUP BY 
         EmployeeID) AS S ON e.EmployeeID = S.EmployeeID;

